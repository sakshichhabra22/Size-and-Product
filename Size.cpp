#include <iostream>
using namespace std;

//Compiler version g++ 6.3.0

int main()
{
    char a;
    cout <<"Size of char: "<< sizeof(a)<<" byte"<<endl;

    int x;
    cout <<"Size of int: "<< sizeof(x)<<" bytes"<<endl;
    float y;
    cout <<"Size of float: "<< sizeof(y)<<" bytes"<<endl;
    double z;
    cout <<"Size of double: "<< sizeof(z)<<" bytes"<<endl;
    return 0;

}
